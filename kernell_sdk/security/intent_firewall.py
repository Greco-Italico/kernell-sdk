class OrchestratorStub:
    """
    Deterministic AST interpreter with Stateful Provenance Enforcement.
    Executes validated plans. Fails closed on any deviation or runtime taint violation.
    """
    def __init__(self, authority: TokenAuthority, provenance_store=None):
        self.authority = authority
        self.store = provenance_store  # Duck-typed KV store (Redis or Dict)
        self.ACTION_MAP = {
            ActionType.READ_FILE: self._read_file,
            ActionType.TRANSFORM: self._transform,
            ActionType.WRITE_FILE: self._write_file,
            ActionType.NETWORK_REQUEST: self._network_request,
            ActionType.LIST_DIR: self._list_dir,
            ActionType.NOOP: self._noop,
            # EXECUTE_CODE and VALIDATE are blocked or handled elsewhere
        }
    
    def execute(self, plan: PlanIR, token: CapabilityToken) -> Dict[str, Any]:
        # Step 1: Mandatory Re-hash (Drift Prevention)
        computed_hash = plan.hash_plan()
        if computed_hash != token.plan_hash:
            raise PlanDriftError("Plan hash mismatch. Plan was mutated after signing.")
            
        # Step 2: Cryptographic Verification (Parent Only)
        # If authority is None, we are inside the untrusted worker and assume Parent already verified.
        if self.authority is not None:
            if not self.authority.verify_token(token):
                raise InvalidCapabilityToken("Token signature is invalid.")
            
        # Step 3: Explicit Expiration Check (Defense in depth)
        if time.time() > token.expires_at:
            raise InvalidCapabilityToken("Token has expired.")
            
        # Step 4: Strict Interpreter (No Shell, No Eval) with Dynamic Taint Tracking
        # runtime_values stores tuples: (value, dynamic_sensitivity)
        runtime_values = {}
        execution_log = []
        
        for step in plan.steps:
            handler = self.ACTION_MAP.get(step.action)
            if not handler:
                raise UnsupportedActionError(f"Action {step.action} is not supported by strict interpreter.")
                
            # Execute AST node. Handler returns (result_value, result_sensitivity)
            res_val, res_sens = handler(step, runtime_values)
            
            # Map outputs to runtime values
            if res_val is not None and step.outputs:
                for out_node in step.outputs:
                    runtime_values[out_node] = (res_val, res_sens)
                    
            execution_log.append(f"Executed {step.action} -> sensitivity: {res_sens}")
            
        return {
            "status": "success",
            "log": execution_log,
            "runtime_state": runtime_values
        }

    def _resolve_inode(self, path: str) -> str:
        import os
        try:
            st = os.stat(path)
            return f"{st.st_dev}:{st.st_ino}"
        except FileNotFoundError:
            # For new files, we return a virtual path identifier until created.
            # In a real system, the file is created first, then stat'd.
            return f"virtual:{path}"

    def _read_file(self, step: PlanStep, state: Dict[str, Any]) -> tuple:
        path = step.params.get("path", "/dev/null")
        inode = self._resolve_inode(path)
        
        # Base sensitivity is INTERNAL for any filesystem read
        base_level = DataSensitivity.INTERNAL.level
        
        persisted_level = 0
        if self.store is not None:
            persisted_level = self.store.get(inode) or 0
            
        effective_level = max(base_level, persisted_level)
        
        # Return mock value and its dynamic sensitivity
        return f"file_content({inode})", effective_level
        
    def _transform(self, step: PlanStep, state: Dict[str, Any]) -> tuple:
        # Dynamic Taint Propagation: output gets max of inputs
        max_in_sens = 0
        inputs_data = []
        for inp in step.inputs:
            val, sens = state.get(inp, ("empty", 0))
            inputs_data.append(val)
            max_in_sens = max(max_in_sens, sens)
            
        return f"transformed({','.join(inputs_data)})", max_in_sens
        
    def _write_file(self, step: PlanStep, state: Dict[str, Any]) -> tuple:
        path = step.params.get("path", "/dev/null")
        
        # Calculate sensitivity of data being written
        max_in_sens = 0
        for inp in step.inputs:
            _, sens = state.get(inp, (None, 0))
            max_in_sens = max(max_in_sens, sens)
            
        # In a real system, create file if not exists here to get inode
        inode = self._resolve_inode(path)
        
        if self.store is not None:
            existing_level = self.store.get(inode) or 0
            new_level = max(existing_level, max_in_sens)
            self.store.set(inode, new_level)
            
        return "mock_write_success", max_in_sens
        
    def _network_request(self, step: PlanStep, state: Dict[str, Any]) -> tuple:
        # Runtime Egress Firewall
        max_in_sens = 0
        for inp in step.inputs:
            _, sens = state.get(inp, (None, 0))
            max_in_sens = max(max_in_sens, sens)
            
        if max_in_sens >= DataSensitivity.INTERNAL.level:
            raise Exception("RUNTIME EXFILTRATION BLOCKED: Attempted to send sensitive data over network.")
            
        return "mock_network_success", max_in_sens
        
    def _list_dir(self, step: PlanStep, state: Dict[str, Any]) -> tuple:
        # Base sensitivity for reading directory metadata
        return "mock_dir_list", DataSensitivity.INTERNAL.level
        
    def _noop(self, step: PlanStep, state: Dict[str, Any]) -> tuple:
        return "noop", 0

